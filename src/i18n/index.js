export function useI18n() {
  const t = (key) => {
    const msg = messages[locale.value]
    if (msg && msg[key]) {
      return msg[key]
    }
    return key
  }

  const lang = computed(() => locale.value)

  return { t, lang, locale: lang }
}

export const i18n = { useI18n }
